Pratique algumas vezes cada posição e descanse no momento presente ao final

Posição da montanha
Posicione os pés na largura do quadril, coluna reta, joelhos destravados, ombros relaxados e cóccix ligeiramente abaixado. Quando você inspirar, levante os braços e quando expirar volte-os para baixo pela lateral observando o peso dos braços, até alcançar os quadris.

“Tirando uma camiseta”
Na inspiração, vá movendo seus braços para cima, como se estivesse tirando uma camiseta e na expiração vá terminando o movimento.

Tirando uma camiseta ao contrário
Na inspiração vá subindo os braços pela lateral e na expiração cruzando-os à frente como um ‘x’.

Colhendo uma fruta
