Existem muitas coisas que nós não controlamos e muitas outras que não acontecem “do nosso jeito”. Nós podemos escolher, entretanto, como respondemos e como experimentamos a vida. Praticando atenção plena (mindfulness) regularmente ficamos menos automáticos e reativos e mais conscientes em nossas escolhas o que, em última análise, nos dá mais liberdade. Cuidar de si e se engajar em atividades que te energizam faz parte da busca pelo equilíbrio em nossas vidas. Manter uma prática não é fácil. Dificuldades e barreiras vão aparecer. Seja gentil com você mesmo. Lembre-se de que qualquer prática é uma boa prática; você sempre pode começar de novo a partir de onde você está.

