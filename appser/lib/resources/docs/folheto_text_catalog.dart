DESAFIOS COMUNS NA PRÁTICA DE MEDITAÇÃO (E NAS NOSSAS VIDAS DIÁRIAS)
Existem alguns desafios que aparecem muito frequentemente no andamento da prática de meditação, a lista desses desafios permanece a mesma ao longo de milhares de anos. Essas experiências não são ruins ou erradas; são simplesmente parte da prática de meditação, e não significam que sua meditação “não funciona” ou que você está fazendo a prática incorretamente. Aprender a reconhecer essas experiências, como elas aparecem e saber que elas são simplesmente parte da experiência da prática de meditação pode ajudar. Não é só com você! Ao aprender a reconhecer esses desafios na sua prática, nós podemos também aprender a reconhecê-los na nossa vida diária e notar, também, os meios com os quais tendemos a reagir a eles.

Aversão
Esta é a experiência de “não querer”. Em algum momento nós experimentamos coisas e temos uma reação de repúdio ou o desejo de fazer aquela experiência ir embora. Isso pode ser descrito como “aversão”. Ela pode incluir sentimentos de medo, raiva, irritação, desgosto ou ressentimento.

Desejos ou vontade
Esta é a experiência de “querer”. Ela pode ser tão sutil quanto a expectativa de se sentir relaxado e calmo ou extrema como um desejo intenso de usar uma substância.

Inquietação ou agitação
Este pode ser um tipo de desconforto inquieto. Ele pode ser experimentado fisicamente, como um forte desejo de se mover durante a meditação, ou como uma agitação mental, em que a mente se sente inquieta ou desconfortável.

Sonolência ou torpor
Este pode ser moleza física ou lentidão mental. Pode ocorrer na mente, no corpo ou em ambos.

Dúvida
A dúvida pode ser vivida como uma sensação de dúvida pessoal (“Eu não consigo fazer esta prática”) ou dúvida sobre a prática e sua utilidade (“Isso é ridículo. Por que as pessoas somente se sentariam naquele lugar e observariam sua respiração?”). A incerteza é um desafio especialmente complicado porque pode ser muito convincente. Se lembrar de que a meditação tem sido, por volta de milhares de anos, uma forma que tem ajudado milhões de pessoas a transformar suas vidas pode te ajudar a sair da dúvida. Não existe alguém que não possa participar da meditação, ela é acessível a qualquer pessoa que deseja praticar. O importante é ficar com isso em mente e quando os desafios chegarem, trazê-los também para sua consciência.

